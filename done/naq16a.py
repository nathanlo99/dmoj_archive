trans = ["@", "8", "(", "|)", "3", "#", "6", "[-]", "|", "_|", "|<", "1",
         "[]\\/[]", "[]\\[]", "0", "|D", "(,)", "|Z", "$", "']['", "|_|", "\\/",
         "\\/\\/", "}{", "`/", "2"]

for c in input().strip():
    if c.isalpha():
        print(trans[ord(c.upper()) - ord("A")], end="")
    else:
        print(c, end="")